# type casting = conver the data type of a value to another data type.

x = 6 #integer
y = 2.0 #float
z = "3" #string

x = str(x) # converts the integer to string
y = int(y) # converts the floating point number to a integer
z = float(z) # converts the string to a float

print("The value of the variable x is "+x)
print(y*y)
print(z*3)

# You can also do this directly in the print statement, but this is not permanent to the variable.
print(int(x)*5)
